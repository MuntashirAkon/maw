#include <stdlib.h>
#include <string.h>
#include "mawdefs.h"

INT * LCParray ( unsigned char * text, INT n, INT * SA, INT * ISA )
{
	INT i=0, j=0;

	INT * LCP = ( INT * ) calloc  ( (n+1), sizeof( INT ) );

	LCP[0] = 0;
	for ( i = 0; i < n; i++ ) // compute LCP[ISA[i]]
		if ( ISA[i] != 0 )
		{
			if ( i == 0) j = 0;
			else j = (LCP[ISA[i-1]] >= 2) ? LCP[ISA[i-1]]-1 : 0;
			while ( text[i+j] == text[SA[ISA[i]-1]+j] )
				j++;
			LCP[ISA[i]] = j;
		}
    	LCP[n]=0;
	return ( LCP );
}
